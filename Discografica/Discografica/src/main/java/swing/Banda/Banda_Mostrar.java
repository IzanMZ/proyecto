    public Banda_Mostrar() {

        // Inicializa todos los componentes gráficos del formulario
        initComponents();

        // Se crea el modelo de la tabla con sus columnas
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Codigo", "Nombre", "Años actuando", "Origen", "Genero"
                }
        );

        // Se asigna el modelo a la tabla
        completaTBL.setModel(modelo);

        try {

            // Se obtienen todos los registros de Banda desde la base de datos
            String[][] matrizMostrar = Sql_banda.consultarTabla();

            // Validación: si no hay datos, se lanza excepción controlada
            if (matrizMostrar == null || matrizMostrar.length == 0) {
                throw new Excepciones("No hay datos para mostrar");
            }

            // Recorre la matriz de resultados
            for (int i = 0; i < matrizMostrar.length; i++) {

                // Validación de fila válida (evita filas nulas o incompletas)
                if (matrizMostrar[i] == null || matrizMostrar[i].length < 5) {
                    continue;
                }

                // Se añade cada fila al modelo de la tabla
                modelo.addRow(new Object[]{
                    matrizMostrar[i][0],
                    matrizMostrar[i][1],
                    matrizMostrar[i][2],
                    matrizMostrar[i][3],
                    matrizMostrar[i][4]
                });
            }

        } catch (Excepciones e) {

            // Mensaje controlado para el usuario
            javax.swing.JOptionPane.showMessageDialog(
                    null,
                    e.getMessage(),
                    "Información",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE
            );

        } catch (Exception e) {

            
            javax.swing.JOptionPane.showMessageDialog(
                    null,
                    "Error al cargar la tabla: " + e.getMessage(),
                    "Información",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE
            );
        }
    }

