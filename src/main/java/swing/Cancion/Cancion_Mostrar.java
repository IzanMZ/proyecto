    public Cancion_Mostrar() {

        // Inicializa los componentes gráficos del formulario
        initComponents();

        // Se crea el modelo de la tabla con sus columnas
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Codigo", "Codigo Album", "Posicion",
                    "Titulo", "Compositor", "Duracion"
                }
        );

        // Se asigna el modelo a la tabla
        cancionTBL.setModel(modelo);

        try {

            // Se consultan todas las canciones desde la base de datos
            String[][] matrizMostrar = Sql_Cancion.consultarTabla();

            // Validación: si no hay datos, se lanza excepción controlada
            if (matrizMostrar == null || matrizMostrar.length == 0) {
                throw new Excepciones("No hay canciones para mostrar");
            }

            int filasInvalidas = 0;

            // Recorre la matriz de resultados
            for (int i = 0; i < matrizMostrar.length; i++) {

                // Validación de fila válida (evita datos nulos o incompletos)
                if (matrizMostrar[i] == null || matrizMostrar[i].length < 6) {
                    filasInvalidas++;
                    continue;
                }

                // Se añade cada fila válida al modelo de la tabla
                modelo.addRow(new Object[]{
                    matrizMostrar[i][0],
                    matrizMostrar[i][1],
                    matrizMostrar[i][2],
                    matrizMostrar[i][3],
                    matrizMostrar[i][4],
                    matrizMostrar[i][5]
                });
            }

        } catch (Excepciones e) {

            // Mensaje de error controlado
            javax.swing.JOptionPane.showMessageDialog(
                    null,
                    e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );

        } catch (Exception e) {

            // Error inesperado del sistema
            javax.swing.JOptionPane.showMessageDialog(
                    null,
                    "Error cargando canciones: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }
    }

